if 780 >= star and star >= 620:
    print('Red')
elif 620 >= star and star >= 590:
    print('Orange')
elif 590 > star and star >= 570:
    print('Yellow')
elif 570 > star and star >= 495:
    print('Green')
elif 495 > star and star >= 450:
    print('Blue')
elif 450 > star and star >= 425:
    print('Indigo')
else:
    print('Violet')